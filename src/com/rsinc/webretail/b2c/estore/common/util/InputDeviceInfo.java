package com.rsinc.webretail.b2c.estore.common.util;

/**
 * @author Roshan Titus
 *
 */
public class InputDeviceInfo {

	private String ipAddress;
	private String browserVersion;
	private String browserCode;
	private String browserText;
	private String osVersion;
	private String osCode;
	private String osText;
	
	
	public String getIpAddress() {
		return ipAddress;
	}
	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}
	public String getBrowserVersion() {
		return browserVersion;
	}
	public void setBrowserVersion(String browserVersion) {
		this.browserVersion = browserVersion;
	}
	public String getBrowserCode() {
		return browserCode;
	}
	public void setBrowserCode(String browserCode) {
		this.browserCode = browserCode;
	}
	public String getBrowserText() {
		return browserText;
	}
	public void setBrowserText(String browserText) {
		this.browserText = browserText;
	}
	public String getOsVersion() {
		return osVersion;
	}
	public void setOsVersion(String osVersion) {
		this.osVersion = osVersion;
	}
	public String getOsCode() {
		return osCode;
	}
	public void setOsCode(String osCode) {
		this.osCode = osCode;
	}
	public String getOsText() {
		return osText;
	}
	public void setOsText(String osText) {
		this.osText = osText;
	}
	

}
